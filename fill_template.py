def replace_para(p, new_text, bold=None, size=None):
    """Replace all runs with a single run keeping first run's formatting."""
    if p.runs:
        r0 = p.runs[0]
        old_bold = r0.bold
        old_size = r0.font.size
        old_name = r0.font.name
    else:
        old_bold = None
        old_size = Pt(14)
        old_name = 'Times New Roman'
    # Remove all runs
    for r in list(p._p.findall(W + 'r')):
        p._p.remove(r)
    run = p.add_run(new_text)
    run.bold = bold if bold is not None else old_bold
    run.font.size = size if size is not None else old_size
    run.font.name = old_name or 'Times New Roman'
    return run

